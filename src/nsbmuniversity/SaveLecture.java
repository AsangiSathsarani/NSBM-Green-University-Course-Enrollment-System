    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        SaveLectureDetails ld = new SaveLectureDetails();
        ld.setLecId(0);
        ld.setLecName(txtName.getText());
        ld.setAddress(txtAddress.getText());
        ld.setEmail(txtEmail.getText());
        ld.setTelNo(txtTelNo.getText());
        ld.setDesignation(txtDesignation.getText());
        ld.setDepName(txtDepartment.getText());
        
        if(dbops.lectureDetails(ld)){
            JOptionPane.showMessageDialog(this, "Succesefully!!");
            clearFieald();
        }
        else{
            JOptionPane.showMessageDialog(this, "UnSuccesefully!!");
            clearFieald();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

