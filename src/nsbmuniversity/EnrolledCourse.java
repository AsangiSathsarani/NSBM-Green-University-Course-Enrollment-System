    private void ddSubOneSemOneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ddSubOneSemOneActionPerformed
        try {
                con = (Connection) DriverManager.getConnection(url, username, password);
                pst = con.prepareStatement("SELECT coursedetails.credit , coursedetails.cozFee  FROM coursedetails WHERE coursedetails.subName = '" + ddSubOneSemOne.getSelectedItem().toString() + "' ");

                res = pst.executeQuery();
                while (res.next()) {
                    int credit = res.getInt(1);
                    String fee = res.getString(2);
                    txtone.setText(Integer.toString(credit));
                    txtfeeone.setText(fee);
                
            }
            }catch(Exception e){
                System.out.println(e);
            }
    }//GEN-LAST:event_ddSubOneSemOneActionPerformed

