    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        this.dispose();
        ExameResultDetails erd = new ExameResultDetails();
        erd.setRegId(Integer.parseInt(txtRegId.getText()));
        erd.setName(txtNameIni.getText());
        erd.setDepName(ddDepartment.getSelectedItem().toString());
        erd.setCozId(txtCourseId.getText());
        erd.setGradeofLec(ddLecAssGrade.getSelectedItem().toString());
        erd.setGradeofReport(ddLecRpoGrade.getSelectedItem().toString());
        erd.setGradeofExam(ddLecExmGrade.getSelectedItem().toString());
        erd.setGrafeofPracticalAss(ddPrcAssGrade.getSelectedItem().toString());
        erd.setGradeofProject(ddPrcProGrade.getSelectedItem().toString());
        erd.setGradeofPracticalExam(ddPrcExmGrade.getSelectedItem().toString());
        
        if(dbops.ExamResult(erd)){
            JOptionPane.showMessageDialog(this, "Succsessfully!!");
            clearFeaild();
        }
        else{
            JOptionPane.showMessageDialog(this, "UnSuccsessfully!!");
            clearFeaild();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

