    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        PostGraduate pg = new PostGraduate();
        pg.setRegId(Integer.parseInt(txtRegId.getText()));
        pg.setInstituteName(txtInstituteName.getText());
        pg.setDegreeName(txtDegreeName.getText());
        pg.setYear(Integer.parseInt(txtYear.getText()));
        pg.setGpa(txtGpa.getText());
        pg.setColification(txtColification.getText());
        
        if(dbops.postgraduatedetails(pg)){
            JOptionPane.showMessageDialog(this, "Succsessfully!!");
            clearFeaild();
        }
        else{
            JOptionPane.showMessageDialog(this, "UnSuccsessfully!!");
            clearFeaild();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

