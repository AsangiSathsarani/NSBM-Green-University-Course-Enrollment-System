    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        SaveCourseDetails cd = new SaveCourseDetails();
        cd.setCozId(txtCode.getText());
        cd.setSubName(txtSubName.getText());
        cd.setCredit(Integer.parseInt(txtCredit.getText()));
        cd.setDescription(txtDescription.getText());
        cd.setDepName(ddDepName.getSelectedItem().toString());
        cd.setSemester(Integer.parseInt(txtSem.getText()));
        cd.setLecName(txtLecName.getText());
        cd.setInstructName(txtInsName.getText());
        cd.setLabNo(txtLabNo.getText());
        cd.setLabInsructName(txtLabInsName.getText());
        cd.setsTime(txtLabSTime.getText());
        cd.setfTime(txtLabFTime.getText());
        cd.setCozFee(ddCourseFee.getSelectedItem().toString());
        if(dbops.courseDetails(cd)){
        
            JOptionPane.showMessageDialog(this, "Succesefully!!");
            clearFeaild();
        }
        else{
            JOptionPane.showMessageDialog(this, "unSuccesefully!!");
            clearFeaild();
        }
    }//GEN-LAST:event_btnSaveActionPerformed
